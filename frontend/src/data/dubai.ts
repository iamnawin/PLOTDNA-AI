function boxPolygon(center: [number, number], dLat: number, dLng: number): [number, number][] {
  const [lat, lng] = center
  return [
    [lat + dLat, lng - dLng],
    [lat + dLat, lng + dLng],
    [lat - dLat, lng + dLng],
    [lat - dLat, lng - dLng],
  ]
}

