/** Build a simple rectangle polygon around a center point */
function rect(lat: number, lng: number, d = 0.025): [number, number][] {
  return [
    [lat + d, lng - d],
    [lat + d, lng + d],
    [lat - d, lng + d],
    [lat - d, lng - d],
  ]
}

